package demoWhile;

public class App {

	
	/**
	 * VARIABLES
	 * i est un entier
	 * 
	 * TRAITEMENT
	 * i <-- 0
	 * 
	 * Tant que i <= 10
	 * 	ECRIRE "i vaut ", i
	 * 	i++
	 * FIN TANT QUE
	 */
	public static void main(String[] args) {
		 
		int i = 0;
		
		while(i <= 10) {
			System.out.println("i vaut" + i);
			
				i++;
		}

	}

}
